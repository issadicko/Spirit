    private void filtreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_filtreActionPerformed
            if (!mois.getSelectedItem().toString().equals("0")) {
                if (!jour.getSelectedItem().toString().equals("0")) {
                    System.out.println(jour.getSelectedItem());
                    controler.findByDay(Integer.parseInt(jour.getSelectedItem().toString())
                                       ,Integer.parseInt(mois.getSelectedItem().toString()) ,
                                       Integer.parseInt(annee.getSelectedItem().toString()));
                }else{
                    controler.findByMonth(Integer.parseInt(mois.getSelectedItem().toString()) ,
                                          Integer.parseInt(annee.getSelectedItem().toString()));
                }
            }else{
                controler.findByYear(Integer.parseInt(annee.getSelectedItem().toString()));
            }
    }//GEN-LAST:event_filtreActionPerformed

