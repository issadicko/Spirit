    @Override
    public void upadateBilan(ArrayList<Bilan> pList) {
        ((DefaultTableModel)bilan.getModel()).setRowCount(0);
        
        System.out.println(pList.size());
        
        pList.forEach((bil) -> {
            ((DefaultTableModel)bilan.getModel()).addRow(new Object[]{
                bil.getDesignation(),
                bil.getPrix(),
                bil.getNbvente(),
                bil.getTotale()
            });
        });
    }
    

