                      pu = prix_u.getText();
        
        //On s'assure que les champ ne sont pas vide
        if(code.length() != 0 && pu.length() != 0  && desi.length() != 0){
            
            //ON s'assure que ce produit n'existe pas dans la base de donn√©es
               produit = new Produit(code, desi, Long.parseLong(pu),0);
               this.setVisible(false);
        }
        else {
            JOptionPane.showMessageDialog(this, "Veuillez Renseigner tout les champs !","Attention",JOptionPane.WARNING_MESSAGE);
        }
