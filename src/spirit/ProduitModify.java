    public Produit showMe(String code){
        
        Produit p = controler.getPmanager().find(code);
        
        if (p!=null) {
            
            code_produit.setEnabled(false);
            
            code_produit.setText(p.getCode());
            designation.setText(p.getDesignation());
            prix_u.setText(String.valueOf(p.getPrix()));
            
            this.setVisible(true);
        }
        
        return produit;
    }

